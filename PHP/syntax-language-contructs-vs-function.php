# In PHP, this is a comment.  
#
# PHP has language constructs and functions.  Functions require parenthesis, language constructs do not. 
#
# Example Using Language Construct
#
<?php
echo "I am learning PHP!";
?>
#
# Example Using a function
#
 <?php
function writeMsg() {
  echo "I am learning PHP!";
}

writeMsg(); // call the function
?> 
#
# Note these two PHP statements both have the same output, namely: "I am learning PHP!" but they require different syntax.
#
